# Cost categories for foods
COST_TAGS = {
    'low': [
        'maize', 'ugali', 'sukuma', 'cabbage', 'beans', 'lentils', 
        'green grams', 'dagaa', 'omena', 'sweet potato', 'cassava',
        'banana', 'spinach', 'amaranth', 'managu', 'terere', 'kunde',
        'chapati', 'bread', 'rice', 'milk', 'tea', 'water',
        'potato', 'arrowroot', 'yam', 'taro', 'sorghum', 'millet',
        'cowpeas', 'pigeon peas', 'groundnuts', 'onion', 'tomato'
    ],
    'high': [
        'beef', 'goat', 'lamb', 'pork', 'cheese', 'chicken', 'eggs',
        'butter', 'ice cream', 'almond', 'cashew', 'pistachio',
        'apple', 'orange', 'grape', 'pineapple', 'imported',
        'tuna', 'salmon', 'sausage', 'liver', 'fish fillet'
    ],
}

def get_cost_tag(food_name):
    """Return cost tag for a food (low/high)"""
    food_lower = food_name.lower()
    
    # Check high cost first
    for kw in COST_TAGS['high']:
        if kw in food_lower:
            return 'high'
    
    # Default to low (most Kenyan foods are affordable)
    return 'low'